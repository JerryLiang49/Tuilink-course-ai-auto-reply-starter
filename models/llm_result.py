from pydantic import ConfigDict

from models.base import BaseModel
from typing import Literal, Optional


class ClassifierResult(BaseModel):
    """Structured output returned by the conversation classifier.

    The classifier LLM must choose one category from ``input/categories.json``.
    ``extra="forbid"`` makes the response schema strict so malformed or
    unexpected fields are rejected instead of silently entering the workflow.
    """

    model_config = ConfigDict(
        extra="forbid",
        json_schema_extra={
            "required": ["category", "referenced_message_ids", "confidence", "reason"]
        },
    )

    # Category id selected by the classifier.
    category: str

    # Optional debug score for how confident the LLM is in the selected category.
    confidence: Optional[float] = None

    # Optional debug explanation for why the classifier chose this category.
    reason: Optional[str] = None

    # Message ids most relevant to the classification decision.
    referenced_message_ids: list[str]


class Topic(BaseModel):
    """Single short reply topic suggested by the topic-suggester node."""

    model_config = ConfigDict(
        extra="forbid",
        json_schema_extra={"required": ["topic", "confidence", "reason"]},
    )

    # Short phrase to cover in the generated reply, not a complete sentence.
    topic: str

    # Optional debug score for this suggested topic.
    confidence: Optional[float] = None

    # Optional debug explanation for why this topic was suggested.
    reason: Optional[str] = None


class TopicSuggesterResult(BaseModel):
    """Structured output containing all suggested reply topics."""

    model_config = ConfigDict(
        extra="forbid",
        json_schema_extra={"required": ["topics"]},
    )

    # Ordered list of topics; the prompt asks for at most four.
    topics: list[Topic]


class RequiredAction(BaseModel):
    """One concrete action the referrer requires from the job seeker.

    This is produced by the Action Summarizer when the conversation category
    indicates that the job seeker must provide information or take an action
    before a useful reply can be generated.
    """

    model_config = ConfigDict(
        extra="forbid",
        json_schema_extra={
            "required": [
                "action_id",
                "action",
                "referenced_message_ids",
                "confidence",
                "reason",
            ]
        },
    )

    # Stable snake_case id used to match this requirement with a fulfilled action.
    action_id: str

    # Short description of what the job seeker needs to provide or do.
    action: str

    # Message ids where the referrer requested this action.
    referenced_message_ids: list[str]

    # Optional debug score for this extracted action.
    confidence: Optional[float] = None

    # Optional debug explanation for why this action was extracted.
    reason: Optional[str] = None


class ActionSummarizerResult(BaseModel):
    """Structured output returned by the Action Summarizer node."""

    model_config = ConfigDict(
        extra="forbid",
        json_schema_extra={"required": ["summary", "actions", "prompt_to_user"]},
    )

    # Human-readable summary of all required actions.
    summary: str

    # Required actions/questions the job seeker must fulfill before continuing.
    actions: list[RequiredAction]

    # User-facing prompt asking the job seeker to provide the missing inputs.
    prompt_to_user: str


class FulfilledAction(BaseModel):
    """User-provided answer or evidence for a required action.

    This model is not generated by the LLM. It is passed back into ``State`` by
    the caller after the job seeker supplies the requested information.
    """

    model_config = ConfigDict(extra="forbid")

    # Matches RequiredAction.action_id so the workflow can verify coverage.
    action_id: str

    # True when the user has supplied enough information for this requirement.
    is_fulfilled: bool = True

    # Text answer supplied by the user, such as a job link or role preference.
    answer: str | None = None

    # Extracted text from any uploaded file or attachment relevant to the action.
    attachments_text: str | None = None


class InferencerResult(BaseModel):
    """Structured output returned after fulfilled actions are interpreted.

    The Inferencer turns raw user inputs into a compact conclusion the topic
    suggester and message generator can use, such as whether a referral still
    looks possible and what facts are now available.
    """

    model_config = ConfigDict(
        extra="forbid",
        json_schema_extra={
            "required": [
                "actions_fulfilled",
                "referral_possibility",
                "inference",
                "confidence",
                "reason",
            ]
        },
    )

    # Whether all required actions appear fulfilled after reviewing user input.
    actions_fulfilled: bool

    # High-level interpretation of whether the referral path remains viable.
    referral_possibility: Literal["possible", "not_possible", "unknown"]

    # Concise inferred result to pass into later workflow nodes.
    inference: str

    # Optional debug score for the inference result.
    confidence: Optional[float] = None

    # Optional debug explanation for the inference result.
    reason: Optional[str] = None


class MessageGeneratorResult(BaseModel):
    """Structured output returned by the final message-generator node."""

    model_config = ConfigDict(
        extra="forbid",
        json_schema_extra={
            "required": ["message", "confidence", "reason"]
        },
    )

    # Final message text intended to be sent to the referrer.
    message: str

    # Optional debug score for the generated message.
    confidence: Optional[float] = None

    # Optional debug explanation for the generated message.
    reason: Optional[str] = None
